package controller;

import DAO.accountDAO;
import java.io.IOException;
import java.io.PrintWriter;
import jakarta.servlet.ServletException;
import jakarta.servlet.annotation.WebServlet;
import jakarta.servlet.http.HttpServlet;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import model.hashPasswordMD5;

/**
 *
 * @author Admin
 */
@WebServlet(name = "SetNewPassword", urlPatterns = {"/SetNewPassword"})
public class SetNewPassword extends HttpServlet {

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        try ( PrintWriter out = response.getWriter()) {
            /* TODO output your page here. You may use following sample code. */
            out.println("<!DOCTYPE html>");
            out.println("<html>");
            out.println("<head>");
            out.println("<title>Servlet SetNewPassword</title>");            
            out.println("</head>");
            out.println("<body>");
            out.println("<h1>Servlet SetNewPassword at " + request.getContextPath() + "</h1>");
            out.println("</body>");
            out.println("</html>");
        }
    }

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String newPassword = request.getParameter("newPassword");
        
        String confirmPassword = request.getParameter("confirmPassword");
        
        String emailFix = request.getParameter("emailFix");
        
        if(newPassword.equals(confirmPassword)) {
            // update new password by email
            accountDAO accDAO = new accountDAO();
            // hash new password
            hashPasswordMD5 MD5 = new hashPasswordMD5();
            String hassPass = MD5.hashPasswordMD5(newPassword);
            // update pass to DB
            accDAO.updatePassword(emailFix, hassPass);
            
            request.getRequestDispatcher("/view/common/login.jsp").forward(request, response);
            
        }else {
            request.setAttribute("error", "Confirm Password is not matching with New Password");
            request.getRequestDispatcher("/view/common/FillNewPassword.jsp").forward(request, response);
        }
    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
